package sbnz.web.dto;

public class ExerciseDto {

	private String name;
	
	private String muscle;
	
	public ExerciseDto () {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMuscle() {
		return muscle;
	}

	public void setMuscle(String muscle) {
		this.muscle = muscle;
	}
	
	
}
